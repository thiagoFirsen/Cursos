// Duas expressoes que resultam em TRUE (aritimeticos,relacionais,logicos)

let verdadeiro1 = 5 > 3 && 5 + 4 > 7;
let verdadeiro2 = 2 * 2 != 3 || 5 === 5;

console.log(verdadeiro1, verdadeiro2);

// Duas expressoes que resultam em FALSE(aritimeticos,relacionais,logicos)
let falso1 = 5 < 3 && 5 + 4 < 7;
let falso2 = 2 * 2 === 3 || 5 != 5;
console.log(falso1, falso2);
